export const isValidSubject = (subject) => {
  return GAME_SUBJECTS.includes(subject);
};

export const isValidSkill = (skill) => {
  return GAME_SKILLS.includes(skill);
};

export const searchSubjects = (query) => {
  if (!query) return GAME_SUBJECTS;
  const lowerQuery = query.toLowerCase();
  return GAME_SUBJECTS.filter(subject =>
    subject.toLowerCase().includes(lowerQuery)
  );
};

export const searchSkills = (query) => {
  if (!query) return GAME_SKILLS;
  const lowerQuery = query.toLowerCase();
  return GAME_SKILLS.filter(skill =>
    skill.toLowerCase().includes(lowerQuery)
  );
};